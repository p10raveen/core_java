package com.stringexamples;

public class Example4 {
    public static void main(String[] args)
    {
        String a = "Hello";
        System.out.println(a.charAt(0)); // H

        String b = "Hello";
        System.out.println(b.charAt(4)); // o

        String c = "Hello";
        System.out.println(c.charAt(c.length()-1)); // o

        String d = "Hello";
        System.out.println(d.charAt(d.length())); // SIOOBE
    }
}
